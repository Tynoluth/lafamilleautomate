def lafamilleautomate():
    print("lafamilleautomate est présent. (Tape 'exit' pour quitter.)")

    while True:
        user_input = input("vous: ").lower().strip() 
        #commentaire lafamilleautomate : .lower permet de tout mettre en minuscule pour que le chat reconnaisse tout. .strip permet de nettoyer les espaces

        if user_input == "exit":
            print("lafamilleautomate: à bientot")
            break
            #commentaire lafamilleautomate : break permet d'arreter la boucle

        reponse = None
        #commentaire lafamilleautomate : pour éviter les erreurs si des mots ne sont pas dans les réponses

        for cle in reponses:
            if cle in user_input:
                reponse = reponses[cle]
                break

        if reponse:
            print(f"lafamilleautomate: {reponse}")
        else:
            print("lafamilleautomate: tape aide car je ne comprends pas")

if __name__ == "__main__":
    lafamilleautomate()






